public class mathfunction {
    //addition
    public static int addition(int num1, int num2){
        return num1 + num2;
    }
    //subtraction
    public static int subtract(int num1, int num2){
        return num1 - num2;
    }
    //multiply
    public static int multiply(int num1, int num2){
        return num1 * num2;
    }
    //divide
    public static int divide(int num1, int num2){
        return num1 / num2;
    }
}
